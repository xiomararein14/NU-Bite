function emptyStateMessage(text) {
  return `<div class="empty-message"><span class="empty-message-icon" data-icon="inbox" aria-hidden="true"></span><p>${text}</p></div>`;
}

function icon(name, extraClass = "icon icon-md") {
  const paths = ICON_PATHS[name];
  if (!paths) return "";
  return `<span class="${extraClass}" aria-hidden="true"><svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${paths}</svg></span>`;
}

function renderIcons(root = document) {
  root.querySelectorAll("[data-icon]").forEach(el => {
    const name = el.dataset.icon;
    if (!ICON_PATHS[name]) return;
    const size = el.dataset.iconSize || "md";
    if (!el.classList.contains("icon")) {
      el.classList.add("icon", `icon-${size}`);
    }
    el.innerHTML = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${ICON_PATHS[name]}</svg>`;
  });
}

function starsHtml(rating, max = 5) {
  let html = '<span class="feedback-stars-inline" aria-hidden="true">';
  for (let i = 1; i <= max; i++) {
    const filled = i <= Math.round(rating);
    html += `<span class="${filled ? "" : "muted"}">${icon("star", "icon icon-sm")}</span>`;
  }
  return `${html}</span>`;
}

function trackStepIcon(done) {
  return done ? icon("check", "icon icon-sm") : icon("circle", "icon icon-sm");
}

document.addEventListener("DOMContentLoaded", () => renderIcons());
